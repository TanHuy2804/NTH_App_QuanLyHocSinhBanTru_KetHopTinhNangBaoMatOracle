    void closeMenubar(){
        new Thread(new Runnable(){
            @Override
            public void run()
            {
                for(int i=width; i>0;i--)
                {
                    side.setSize(i,hieght);
                       try {
                        Thread.sleep(2);
                    } catch (InterruptedException ex) {
                        Logger.getLogger(TrangChu_GUI.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }
            }).start();
    }
