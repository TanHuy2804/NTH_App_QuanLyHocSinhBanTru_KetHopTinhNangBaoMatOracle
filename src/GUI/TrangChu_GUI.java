    public class CheckSessionWorker extends SwingWorker<Boolean, Void> {
        private final TrangChu_GUI TrangChu_GUI; // Tham chiếu đến JFrame TrangChu_GUI

        public CheckSessionWorker(TrangChu_GUI TrangChu_GUI) {
            this.TrangChu_GUI = TrangChu_GUI;
        }

        @Override
        protected Boolean doInBackground() throws Exception {
            boolean sessionExists = true;
            while (sessionExists) { // Kiểm tra liệu có bị hủy không
                try {
                    String maNV = Login_GUI.loggedInUser;
                    sessionExists = DAO_KiemTraSession.KiemTraSession(maNV);
                } catch (Exception e) {
                    
                }
                Thread.sleep(5000); // Ngủ trong 5 giây
            }
            return sessionExists;
        }

        @Override
        protected void done() {
            try {
                boolean sessionValid = get(); // Lấy kết quả từ doInBackground()
                if (!sessionValid) {
                    // Nếu phiên không còn tồn tại, thông báo và đóng ứng dụng
                    TrangChu_GUI.dispose();
                    System.exit(0);
                }
            } catch (Exception ex) {
                // Xử lý ngoại lệ nếu cần
            }
        }
    }
    
