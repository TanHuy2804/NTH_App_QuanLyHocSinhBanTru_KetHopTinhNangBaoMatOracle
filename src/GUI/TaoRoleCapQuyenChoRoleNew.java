    private void btn_capRoleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_capRoleActionPerformed
        // TODO add your handling code here:
        String tenBang = cbo_tenBang.getSelectedItem().toString().trim();
        String tenRole = cbo_tenRole.getSelectedItem().toString().trim();

        if (ckb_select.isSelected() || ckb_update.isSelected() || ckb_insert.isSelected() || ckb_delete.isSelected()) {
            if (ckb_select.isSelected()) {
                NhanVien_DAO.capQuyen("SELECT", tenBang, tenRole);
            }
            if (ckb_update.isSelected()) {
                NhanVien_DAO.capQuyen("UPDATE", tenBang, tenRole);
            }
            if (ckb_insert.isSelected()) {
                NhanVien_DAO.capQuyen("INSERT", tenBang, tenRole);
            }
            if (ckb_delete.isSelected()) {
                NhanVien_DAO.capQuyen("DELETE", tenBang, tenRole);
            }
            JOptionPane.showMessageDialog(this, "Thực hiện chức năng thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn ít nhất một chức năng", "Thông báo", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btn_capRoleActionPerformed

