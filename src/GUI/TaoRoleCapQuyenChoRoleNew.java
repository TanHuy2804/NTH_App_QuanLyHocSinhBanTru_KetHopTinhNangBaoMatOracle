    public void laydulieuNV(ArrayList<GRANTED_ROLE_POJO> dsROLE) {
        tblData.removeAllElements();
        for (GRANTED_ROLE_POJO b : dsROLE) {
            Vector v = new Vector();
            v.add(b.getGRANTED_ROLE());           
            tblData.add(v);
        }
        tb_dsRole.setModel(new DefaultTableModel(tblData, tblTitle));
    }
    
