    public void laydulieuQuyenRole(ArrayList<DBA_SYS_PRIVS> dsg) {
        tblData2.removeAllElements();
        for (DBA_SYS_PRIVS b : dsg) {
            Vector v = new Vector();
            v.add(b.getGRANTEE());
            v.add(b.getOWNER());
            v.add(b.getTABLE_NAME());
            v.add(b.getGRANTOR());
            v.add(b.getPRIVILEGE());
            tblData2.add(v);
        }
        tb_quyenRole.setModel(new DefaultTableModel(tblData2, tblTitle2));
    }
    
