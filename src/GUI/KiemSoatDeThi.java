    private void tableThongTinDeThi(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableThongTinDeThi
        // Kiểm tra xem người dùng đã chọn hàng chưa
        int rowIndex = tb_deThi.getSelectedRow();
        if (rowIndex >= 0) {
            // Lấy mô hình của bảng
            DefaultTableModel model = (DefaultTableModel) tb_deThi.getModel();

            // Lấy thông tin từ hàng được chọn
            String maDeThi = model.getValueAt(rowIndex, 0).toString();
            Object trangThaiObject = model.getValueAt(rowIndex, 6);

            // Kiểm tra xem giá trị có null không trước khi gán cho combobox
            if (trangThaiObject != null) {
                String trangThai = trangThaiObject.toString();

                // Hiển thị thông tin lên các textfield và combobox tương ứng
                txt_maDeThi.setText(maDeThi);

                // Chọn trạng thái tương ứng trong combobox
                cbo_trangThai.setSelectedItem(trangThai);
            } else {
                // Hiển thị thông tin lên các textfield và combobox tương ứng
                txt_maDeThi.setText(maDeThi);
                // Nếu giá trị là null, bạn có thể thiết lập giá trị mặc định hoặc để combobox không chọn bất kỳ giá trị nào
                cbo_trangThai.setSelectedIndex(-1); // Chọn không có mục nào
            }
        }
    }//GEN-LAST:event_tableThongTinDeThi

