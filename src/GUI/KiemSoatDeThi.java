    public void laydulieuDeThi(ArrayList<KiemSoatDeThi_POJO> KiemSoatDeThi) {
        tblData.removeAllElements();
        for (KiemSoatDeThi_POJO b : KiemSoatDeThi) {
            Vector v = new Vector();
            v.add(b.getMaDeThi());
            v.add(b.getNgaySoanDe());
            v.add(b.getSoLuongCauHoi());
            v.add(b.getTrangThai());
            v.add(b.getMaNV());
            v.add(b.getMaMH());
            v.add(b.getTrangThaiTruyCapDe());
            tblData.add(v);
        }
        tb_deThi.setModel(new DefaultTableModel(tblData, tblTitle));
    }
