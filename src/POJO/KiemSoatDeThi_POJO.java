public class KiemSoatDeThi_POJO {
    String MaDeThi, NgaySoanDe, SoLuongCauHoi, TrangThai, MaNV, MaMH, TrangThaiTruyCapDe;

    public KiemSoatDeThi_POJO(String MaDeThi, String NgaySoanDe, String SoLuongCauHoi, String TrangThai, String MaNV, String MaMH, String TrangThaiTruyCapDe) {
        this.MaDeThi = MaDeThi;
        this.NgaySoanDe = NgaySoanDe;
        this.SoLuongCauHoi = SoLuongCauHoi;
        this.TrangThai = TrangThai;
        this.MaNV = MaNV;
        this.MaMH = MaMH;
        this.TrangThaiTruyCapDe = TrangThaiTruyCapDe;
    }

    public String getMaDeThi() {
        return MaDeThi;
    }

    public void setMaDeThi(String MaDeThi) {
        this.MaDeThi = MaDeThi;
    }

    public String getNgaySoanDe() {
        return NgaySoanDe;
    }

    public void setNgaySoanDe(String NgaySoanDe) {
        this.NgaySoanDe = NgaySoanDe;
    }

    public String getSoLuongCauHoi() {
        return SoLuongCauHoi;
    }

    public void setSoLuongCauHoi(String SoLuongCauHoi) {
        this.SoLuongCauHoi = SoLuongCauHoi;
    }

    public String getTrangThai() {
        return TrangThai;
    }

    public void setTrangThai(String TrangThai) {
        this.TrangThai = TrangThai;
    }

    public String getMaNV() {
        return MaNV;
    }

    public void setMaNV(String MaNV) {
        this.MaNV = MaNV;
    }

    public String getMaMH() {
        return MaMH;
    }

    public void setMaMH(String MaMH) {
        this.MaMH = MaMH;
    }

    public String getTrangThaiTruyCapDe() {
        return TrangThaiTruyCapDe;
    }

    public void setTrangThaiTruyCapDe(String TrangThaiTruyCapDe) {
        this.TrangThaiTruyCapDe = TrangThaiTruyCapDe;
    }
    
}
